package org.example.airport_management_system_with_hibernate.connections.model;

import jakarta.persistence.*;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;

@Entity
@Table(name = "passengers")
public class Passenger {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "passenger_id")
    private int id;
    @Column(name = "passenger_name")
    private String name;
    @Column(name = "passenger_phone")
    private String phone;
    @Column(name = "country")
    private String country;
    @Column(name = "city")
    private String city;

    /**
     * Default constructor;
     */
    public Passenger() {

    }

    /**
     * @param name
     * @param phone
     * @param country
     * @param city
     */
    public Passenger(String name, String phone, String country, String city) {
        super();
        this.name = name;
        this.phone = phone;
        this.country = country;
        this.city = city;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the country
     */
    public String getCountry() {
        return country;
    }

    /**
     * @param country the country to set
     */
    public void setCountry(String country) {
        this.country = country;
    }

    /**
     * @return the city
     */
    public String getCity() {
        return city;
    }

    /**
     * @param city the city to set
     */
    public void setCity(String city) {
        this.city = city;
    }

    @Override
    public String toString() {
        return "Passenger [id = " + id + ", name = " + name + ", phone = " + phone + ", country = " + country + ", city = " + city
                + "]\n";
    }



}
